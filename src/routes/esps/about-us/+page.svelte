<h1>Who Are We</h1>
<p>We are Evergreen Student Problem Solvers (ESPS), a group of concerned students and educators. We are collaborating to
    solve problems within Evergreen Public Schools (EPS), specifically making a big impact on budget cuts.
    <p>Please <a href="{base}/esps/join-us">Join us</a> in solving the problems of the Evergreen School District. Your voice matters and
    you can make change.</p>
<p>Although EPS has been successful in passing recent levies, the last Levy passed by a narrow margin. There are real
    concerns about the success of future levies. The next Levy will be on the ballot in two years. We need to educate our
    community about the need for additional school funding.</p>
<p>Evergreen Public Schools has made budget cuts for the last 3 consecutive years. Cuts between $16 million and $20
    million to balance the budget for the '24-'25 school year. This is not acceptable and negatively impacts all student
    learners. But there is hope through action!</p>
<p>We need community members to vote in favor of the next EPS levy. Ensuring children receive the best education
    possible is the responsibility of the entire community, not just families with students currently enrolled. The
    children being educated in our current public school system will one day be the legislators, care-takers, and
    providers of goods and services for our aging population, and will have a huge impact on our society as a whole.</p>
<p>So we ask that you start by <a href="{base}/esps/join-us">joining the ESPS</a> in sharing your feedback about the budget cuts
    on the EPS website. If
    you are a student looking to save a program, an educator seeking support in teaching, or a community member looking
    to be more involved, this is your opportunity.</p>

